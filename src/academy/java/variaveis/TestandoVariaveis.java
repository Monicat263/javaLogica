package academy.java.variaveis;

public class TestandoVariaveis {
    public static void main(String[] args) {
        char masculino = 'M';
        char feminino = 'F';
        String masculino2 = "Masculino";
        String feminio2 = "Feminino";

        boolean condicao = false;
        boolean condicao1 = true;

        System.out.println("tipo char: " + masculino);
        System.out.println("tipo char: " + feminino);
        System.out.println("tipo String: " + masculino2);
        System.out.println("tipo String: " + feminio2);
        System.out.println("tipo boolean falso(0): " + condicao);
        System.out.println("tipo char boolean true(1) : " + condicao1);

    }
}
